package basics;

public class updatingVariables {
    public static void main(String[] args) {
        int x;
        x =8;
        System.out.println(x);
        x =10;
        System.out.println(x);
        x += 7;  //x=x+7
        System.out.println(x);
        x -= 5;  //x=x-5
        System.out.println(x);
    }
}
